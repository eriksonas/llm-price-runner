class TestQualityFallback:
    """quality_index_0_100 must keep fallback strictly below AA frontier."""

    def test_uses_aa_index_when_present(self):
        assert quality_index_0_100({"aa_index": 42.0}) == 42.0

    def test_fallback_caps_below_frontier(self):
        # Even an extreme seeded quality_score must not exceed the cap.
        assert quality_index_0_100({"quality_score": 10.0}) <= 55.0

    def test_fallback_floor_is_zero(self):
        # Negative / tiny scores clamp to 0, not negative.
        assert quality_index_0_100({"quality_score": 1.0}) == 0.0

    def test_old_bug_no_longer_dominates(self):
        # The pre-fix fallback returned 98 for q=9.8 and would outrank AA frontier (~57).
        fallback = quality_index_0_100({"quality_score": 9.8})
        frontier = quality_index_0_100({"aa_index": 57.0})
        assert fallback < frontier


