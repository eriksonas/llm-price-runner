class TestLatencyPenalty:
    """Penalty must be symmetric log around the knee."""

    def test_zero_at_knee(self):
        for sensitivity, knee in LATENCY_KNEE.items():
            assert latency_penalty(knee, sensitivity) == pytest.approx(0.0, abs=1e-9)

    def test_bonus_below_knee(self):
        # Sub-knee latency earns a negative (bonus) score.
        assert latency_penalty(10, "interactive") < 0
        # And the bonus is bounded — even latency_ms=0 stays above -1.
        assert latency_penalty(0, "interactive") > -1.0

    def test_penalty_above_knee(self):
        assert latency_penalty(2000, "interactive") > 1.0

    def test_one_decade_above_knee_is_about_one(self):
        # At 10× knee, the penalty should be close to +1 (log10 of ~10).
        ten_x = 10 * LATENCY_KNEE["interactive"]
        # The (knee/4) shift makes this slightly less than 1; allow a band.
        assert 0.7 < latency_penalty(ten_x, "interactive") < 1.0


class TestArenaMatch:
    def test_explicit_map_wins(self):
        arena = {"gpt-4o-2024-08-06": 1305.0}
        m = {"id": "openai-gpt-4o", "model": "GPT-4o"}
        assert _match_arena_elo(m, arena) == 1305.0

    def test_no_substring_collision(self):
        # The old bug: 'gpt-4' substring would match 'gpt-4o' for the gpt-4.1 model.
        arena = {
            "gpt-4o-2024-08-06":     1305.0,
            "gpt-4.1-2025-04-14":    1413.0,
        }
        m41 = {"id": "openai-gpt-4-1", "model": "GPT-4.1"}
        assert _match_arena_elo(m41, arena) == 1413.0  # not 1305

    def test_unmapped_returns_none(self):
        arena = {"gpt-4o-2024-08-06": 1305.0}
        m = {"id": "totally-new-model", "model": "Totally New"}
        assert _match_arena_elo(m, arena) is None

    def test_arena_names_keys_are_strings(self):
        # Catch typos in the explicit map at test time.
        for key, val in ARENA_NAMES.items():
            assert isinstance(key, str) and key
            assert isinstance(val, str) and val
