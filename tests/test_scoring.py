@pytest.mark.parametrize("workload,sensitivity,city", CASES)
def test_scoring_snapshot(workload, sensitivity, city):
    key = f"{workload}|{sensitivity}|{city}"
    expected_rows = EXPECTED[key]
    for sample, expected in zip(SAMPLE_MODELS, expected_rows):
        actual = compute_model_score(dict(sample), workload, sensitivity, city)
        assert sample["id"] == expected["id"]
        for field in SNAPSHOT_FIELDS:
            actual_v = actual[field]
            expected_v = expected[field]
            if isinstance(expected_v, float):
                assert actual_v == pytest.approx(expected_v, abs=0.01), (
                    f"{key} / {sample['id']} / {field}: {actual_v} != {expected_v}"
                )
            else:
                assert actual_v == expected_v, (
                    f"{key} / {sample['id']} / {field}: {actual_v!r} != {expected_v!r}"
                )


